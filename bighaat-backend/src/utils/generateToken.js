import jwt from "jsonwebtoken";

const generateToken = (user) => {
    return jwt.sign(
        { id: user.id, email: user.email, role: user.role },
        process.env.JWT_SECRET || "bighaat_secret",
        { expiresIn: process.env.JWT_EXPIRES_IN || "7d" }
    );
};

export default generateToken;
